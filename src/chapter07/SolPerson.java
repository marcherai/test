package chapter07;

public class SolPerson {

	public String name;
	public float height;
	public float weight;

	public SolPerson() {

	}

	public SolPerson(String name) {

		this.name = name;

	}

	public SolPerson(String name, float height, float weight) {

		this.name = name;
		this.height = height;
		this.weight = weight;
	}

}
